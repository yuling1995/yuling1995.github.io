//二级联动
function nav(){
	var navItem = $('.nav-item');
	var itemHeader = $('.item-header');
	var itemBody = $('.item-body');
	itemHeader.on('mouseover', function() {
		for(var i = 0; i < itemBody.length; i++) {
			itemBody[i].style.display = 'none';
		}
		this.nextElementSibling.style.display = 'block';
	})
	navItem.on('mouseleave', function() {
		for(var i = 0; i < itemBody.length; i++) {
			itemBody[i].style.display = 'none';
		}
	})
}



